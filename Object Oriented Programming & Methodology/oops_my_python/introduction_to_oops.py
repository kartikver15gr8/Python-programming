class Number:           # Here we defined a class Number"
    def sum(self):      # Here we defined a function i.e "sum"
        return self.a+ self.b   # this sum function will return sum of a and b
    
num = Number()          # Here we created an object "num" which takes "Number" 
num.a = 34              # Here we provided a and b their values
num.b = 66
s = num.sum()      # Here we defined "s" as the summation of a and b
print(s)           # Here we prints the sum which we defined as "s"









